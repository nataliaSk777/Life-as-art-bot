import { getOrCreateUser } from '../database/queries.js';
import { heavyKeyboard } from '../keyboards/main.js';

export function registerSupportHandlers(bot) {
  bot.hears('🆘 Если тяжело', async (ctx) => {
    await getOrCreateUser(ctx);

    await ctx.reply(
      'Я рядом.\n\nЧто сейчас ближе всего?',
      heavyKeyboard()
    );
  });

  bot.action(/^heavy:(.+)$/, async (ctx) => {
    const state = ctx.match[1];

    await ctx.answerCbQuery();

    const replies = {
      'тревожно': 'Сейчас не нужно решать всю жизнь целиком.\n\nПоставь обе стопы на пол. Медленно выдохни. Назови 3 предмета, которые видишь рядом.',
      'одиноко': 'Пусть сейчас рядом будет хотя бы один маленький знак жизни.\n\nНалей тёплой воды, укрой плечи, напиши одно честное предложение: «мне сейчас…»',
      'устала': 'Тебе не нужно становиться сильнее прямо сейчас.\n\nСделай одно действие на восстановление: вода, еда, душ, лечь, выключить лишний звук.',
      'ничего не хочется': 'Тогда не будем хотеть многое.\n\nВыбери действие на 30 секунд: открыть окно, умыться, сесть удобнее, убрать одну вещь.',
      'запуталась': 'Когда всё спутано, возвращаемся к ближайшему шагу.\n\nСпроси себя: что мне нужно сделать в ближайшие 10 минут, а не во всей жизни?'
    };

    await ctx.reply(
      replies[state] || 'Я рядом. Сделай один медленный выдох и выбери самый маленький следующий шаг.'
    );
  });
}
